#data type
'''single value datatype(numeric: int,float,complex and non-numeric:boolean) and multi-value datatype(string,list,tuple,ste,dictionary) '''
#int
a=23
print(a)
print(type(a))
print(id(a))
print(bool(a))
print(float(a))

#float
b=13.5
print(b)
print(type(b))
print(id(b))
print(bool(b))
print(int(b))

#complex
c=(1+8j)
print(c)
print(type(c))
print(id(c))
print(bool(c))

#boolean
d=True
print(d)
print(type(d))
print(id(d))
print(int(d))

#string
e="python"
print(e)
print(type(e))
print(id(e))
print(bool(e))
print(len(e))
print(e[0:5:-1])
print(e[2:5:2])
